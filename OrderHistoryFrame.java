    private void loadOrderedProductDetails(String name) {
        try {
            orderList = OrderDAO.getOrderHistoryDetailsByCustomerId(name);
            if (orderList.isEmpty()) {
                JOptionPane.showMessageDialog(null, "No Products ordered yet");
                this.jtOrderHistory.setEnabled(false);
                return;
            }
            for (OrderPojo order : orderList) {
                Vector<String> v = new Vector<>();
                v.add(order.getOrderId());
                v.add(order.getProductName());
                v.add(order.getProductPrice() + "");
                v.add(order.getDeliveryStaffName());
                v.add(order.getCompanyName());
                v.add(order.getCustomerAddress());
                v.add(order.getReview());
                model.addRow(v);
                System.out.println(v);
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "DB Error in OrderHistoryFrame");
            ex.printStackTrace();
        }
    }
