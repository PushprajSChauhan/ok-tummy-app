    private void jtOrderHistoryMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jtOrderHistoryMouseClicked
        int index=this.jtOrderHistory.getSelectedRow();
        OrderPojo ord = orderList.get(index);
        String review = JOptionPane.showInputDialog(null,"Please enter the review for your delivered product");
        try{
            boolean result = OrderDAO.addReviewToOrders(ord.getOrderId(), review);
            if(!result){
                JOptionPane.showMessageDialog(null,"Review not added");
                return;
            }
            JOptionPane.showMessageDialog(null, "Review Added Successfully!");
            this.dispose();
            new OrderHistoryFrame().setVisible(true);
        }catch(SQLException ex){
            JOptionPane.showMessageDialog(null,"DB Error in OrderHistoryFrame");
            ex.printStackTrace();
        }
    }//GEN-LAST:event_jtOrderHistoryMouseClicked

