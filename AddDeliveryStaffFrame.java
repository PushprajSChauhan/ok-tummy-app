    private void btnAddStaffActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddStaffActionPerformed
        if(!validateInputs()){
            return;
        }
        try{
            StaffPojo staff=new StaffPojo();
            staff.setCompanyId(OwnerProfile.getCompanyId());
            staff.setEmailId(txtEmailId.getText().trim());
            staff.setStaffName(txtName.getText().trim());
            char [] pwd=txtPassword.getPassword();
//            String pwdStr=new String(pwd);
            String pwdStr=String.valueOf(pwd);
//            AB DATABASE MEI PASSWORD KO ENCRYPT KRKE BHEJENGE NA KI PLAIN TEXT JAISE
            pwdStr=PasswordEncryption.getEncryptedPassword(pwdStr);
            staff.setPassword(pwdStr);
            String staffId=StaffDAO.addStaff(staff);
            if(staffId==null){
                JOptionPane.showMessageDialog(null, "Staff not added in the DB");
                return;
            }
            JOptionPane.showMessageDialog(null, " Staff Registration Successfull");
//            YAHA AAGYE MTLB STAFF ADD HOGYA DB MEI AND AB USSE REGISTRATION COMPLETE HONE KA MAIL BHEJENGE
            Map<String,String>emailCredentials=CompanyDAO.getEmailCredentailsByCompanyId(OwnerProfile.getCompanyId());
            System.out.println(emailCredentials.get("emailId"));
            System.out.println(emailCredentials.get("securityKey"));
            Mailer.sendMail(emailCredentials, staff);
            JOptionPane.showMessageDialog(null, "Mail sent successfully to the staff person "+staff.getStaffName());
            clearAll();
        }
        catch(SQLException ex){
            JOptionPane.showMessageDialog(null, "DB Error in AddDeliveryStaff Frame");
            ex.printStackTrace();
        }
        catch(MessagingException ex){
            JOptionPane.showMessageDialog(null, "Error in sending mail");
            ex.printStackTrace();
        }
    }//GEN-LAST:event_btnAddStaffActionPerformed

