    public boolean validateInputs(){
        char [] pwd=txtPassword.getPassword();
        if(pwd.length==0 || txtEmailId.getText().trim().isEmpty() || txtName.getText().trim().isEmpty()){
        JOptionPane.showMessageDialog(null,"Please enter data in all the fields");
        return false;     
        }
        if(Validator.isValidEmail(txtEmailId.getText().trim())==false){
            JOptionPane.showMessageDialog(null,"Please input valid Email ID");
            return false; 
        }
        return true;
    }
    
