    private void loadOrderDetails(String userId) {
        try {
            orderList = OrderDAO.getOrderDetailsByCustomerId(userId);
            if (orderList.isEmpty()) {
                JOptionPane.showMessageDialog(null, "No product present in the cart");
                this.jtCancelOrder.setEnabled(false);
                return;
            }
            for (OrderPojo order : orderList) {
                Vector<String> v = new Vector<>();
                v.add(order.getOrderId());
                v.add(order.getProductName());
                v.add(order.getProductPrice() + "");
                v.add(order.getDeliveryStaffName());
                v.add(order.getCustomerAddress());
                model.addRow(v);
                System.out.println(v);
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "DB Error in CancelOrderFrame");
            ex.printStackTrace();
        }
    }

