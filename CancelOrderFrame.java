    private void jtCancelOrderMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jtCancelOrderMouseClicked
        int index = this.jtCancelOrder.getSelectedRow();
        String [] options = {"Yes","No"};
        int choice = JOptionPane.showOptionDialog(null,"Do you really want to cancel the order?","Select",JOptionPane.DEFAULT_OPTION,JOptionPane.QUESTION_MESSAGE,null,options,options[1]);
        OrderPojo ord = orderList.get(index);
        if(choice == 0){
            try{
                boolean result = OrderDAO.deleteOrderById(ord.getOrderId());
                if(result == false){
                    JOptionPane.showMessageDialog(null, "Cannot cancel the order");
                    return;
                }
                JOptionPane.showMessageDialog(null, "Order cancelled successfully!");
                model.removeRow(index);
                orderList.remove(index);
            }catch(SQLException ex){
                JOptionPane.showMessageDialog(null, "DB Error in removing the product from cart");
                ex.printStackTrace();
            }
        }
        else{
            return;
        }
    }//GEN-LAST:event_jtCancelOrderMouseClicked

